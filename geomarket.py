def calculate_comprehensive_metrics(y_true, y_pred, y_prob=None):
    """
    Расчет комплексных метрик для несбалансированных данных
    """
    metrics = {}

    # Базовые метрики
    metrics['Accuracy'] = accuracy_score(y_true, y_pred)
    metrics['Precision'] = precision_score(y_true, y_pred, zero_division=0)
    metrics['Recall'] = recall_score(y_true, y_pred, zero_division=0)
    metrics['F1'] = f1_score(y_true, y_pred, zero_division=0)
    metrics['MCC'] = matthews_corrcoef(y_true, y_pred)

    # Метрики для несбалансированных данных
    if y_prob is not None and len(np.unique(y_true)) > 1:
        metrics['ROC_AUC'] = roc_auc_score(y_true, y_prob)
        metrics['PR_AUC'] = average_precision_score(y_true, y_prob)
    else:
        metrics['ROC_AUC'] = 0.5
        metrics['PR_AUC'] = np.mean(y_true)

    # Геометрическое среднее (хорошо для несбалансированных данных)
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()
    sensitivity = tp / (tp + fn) if (tp + fn) > 0 else 0
    specificity = tn / (tn + fp) if (tn + fp) > 0 else 0
    metrics['G_Mean'] = np.sqrt(sensitivity * specificity)

    # Balanced Accuracy
    metrics['Balanced_Accuracy'] = (sensitivity + specificity) / 2

    # Комбинированная метрика для несбалансированных данных
    if y_prob is not None:
        metrics['Combined_Score'] = (metrics['F1'] + metrics['PR_AUC'] + metrics['MCC'] + metrics['G_Mean']) / 4
    else:
        metrics['Combined_Score'] = (metrics['F1'] + metrics['MCC'] + metrics['G_Mean']) / 3

    return metrics

