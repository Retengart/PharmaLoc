def create_h3_grid(north, south, east, west, resolution):
    """Создает гексагональную сетку H3 для области"""

    try:
        # Создаем LatLngPoly объект для h3 API (координаты в формате lat, lng)
        from h3 import LatLngPoly

        # Создаем координаты полигона в формате (lat, lng)
        bbox_coords = [
            (south, west),  # SW
            (south, east),  # SE
            (north, east),  # NE
            (north, west),  # NW
            (south, west)   # Замыкаем полигон
        ]

        # Убираем последнюю точку (h3 автоматически замыкает полигон)
        h3_polygon = LatLngPoly(bbox_coords[:-1])

        # Получаем H3 ячейки для полигона
        h3_cells = h3.h3shape_to_cells(h3_polygon, resolution)
        print(f"Создано {len(h3_cells)} H3 ячеек")

        # Создаем GeoDataFrame с ячейками
        cells_data = []
        for cell in h3_cells:
            # Получаем границы ячейки (возвращает список (lat, lng) пар)
            cell_boundary = h3.cell_to_boundary(cell)
            # Конвертируем в полигон Shapely (lng, lat для Shapely)
            cell_polygon = Polygon([(lng, lat) for lat, lng in cell_boundary])

            # Получаем центр ячейки
            cell_center = h3.cell_to_latlng(cell)

            cells_data.append({
                'h3_cell': cell,
                'geometry': cell_polygon,
                'center_lat': cell_center[0],
                'center_lon': cell_center[1]
            })

        h3_gdf = gpd.GeoDataFrame(cells_data)
        h3_gdf.set_geometry('geometry', inplace=True)
        h3_gdf.crs = 'EPSG:4326'

        return h3_gdf

    except Exception as e:
        print(f"Ошибка создания H3 сетки: {e}")
        import traceback
        traceback.print_exc()
        return gpd.GeoDataFrame()

# Создаем сетку
