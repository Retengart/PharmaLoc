    # Создаем булевы признаки наличия каждого типа
    for feature in features:
        if feature in h3_grid.columns:
            h3_grid[f'{feature}_bool'] = (h3_grid[feature] > 0).astype(int)
        else:
            h3_grid[f'{feature}_bool'] = 0

    # Индекс многофункциональности = сумма типов функций в радиусе 500м
    bool_features = [f'{f}_bool' for f in features]
    h3_grid['multifunctionality_index'] = h3_grid[bool_features].sum(axis=1)

    # Убираем промежуточные булевы переменные
    h3_grid.drop(columns=bool_features, inplace=True)

    return h3_grid

