# Функция для расчета размера ячеек H3
def analyze_h3_resolution():
    """Анализирует размеры ячеек H3 для разных разрешений"""
    import h3

    analysis = []
    for res in range(6, 11):  # Разрешения 6-10
        # Получаем размеры ячейки
        edge_length_km = h3.average_hexagon_edge_length(res, 'km')
        area_km2 = h3.average_hexagon_area(res, 'km^2')
        area_m2 = h3.average_hexagon_area(res, 'm^2')

        # Оценка количества ячеек в нашей области
        roi_area_approx = (north - south) * (east - west) * 111 * 111  # км²
        estimated_cells = roi_area_approx / area_km2

        analysis.append({
            'resolution': res,
            'edge_length_km': edge_length_km,
            'area_km2': area_km2,
            'area_m2': area_m2,
            'estimated_cells': int(estimated_cells)
        })

        print(f"Разрешение {res}: край={edge_length_km:.3f} км, "
              f"площадь={area_km2:.6f} км², примерно {int(estimated_cells)} ячеек")

    return analysis

