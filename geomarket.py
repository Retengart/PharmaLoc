def calculate_distance_based_features(h3_grid, poi_data, feature_name, radii=[200, 500, 1000]):
    """Рассчитывает признаки на основе расстояний до объектов"""

    if poi_data.empty:
        for radius in radii:
            h3_grid[f'{feature_name}_count_{radius}m'] = 0
            h3_grid[f'{feature_name}_density_{radius}m'] = 0
        h3_grid[f'{feature_name}_nearest_distance'] = float('inf')
        return h3_grid

    # Получаем центроиды POI если это не точки
    if 'Point' not in poi_data.geometry.geom_type.values:
        poi_points = poi_data.copy()
        poi_points.geometry = poi_points.geometry.centroid
    else:
        poi_points = poi_data[poi_data.geometry.geom_type == 'Point'].copy()

    # Рассчитываем признаки для каждой ячейки
    for idx, cell in h3_grid.iterrows():
        cell_center = Point(cell.center_lon, cell.center_lat)

        # Рассчитываем расстояния до всех POI
        distances = []
        for _, poi in poi_points.iterrows():
            if poi.geometry and not poi.geometry.is_empty:
                # Получаем координаты в зависимости от типа геометрии
                if poi.geometry.geom_type == 'Point':
                    poi_lat, poi_lon = poi.geometry.y, poi.geometry.x
                else:
                    # Для полигонов и других геометрий получаем центроид
                    centroid = poi.geometry.centroid
                    poi_lat, poi_lon = centroid.y, centroid.x

                # Используем geodesic для точного расчета расстояния
                dist = geodesic(
                    (cell.center_lat, cell.center_lon),
                    (poi_lat, poi_lon)
                ).meters
                distances.append(dist)

        if distances:
            # Ближайшее расстояние
            h3_grid.loc[idx, f'{feature_name}_nearest_distance'] = min(distances)

            # Количество объектов в разных радиусах
            for radius in radii:
                count = sum(1 for d in distances if d <= radius)
                h3_grid.loc[idx, f'{feature_name}_count_{radius}m'] = count

                # Плотность (объектов на км²)
                area_km2 = np.pi * (radius/1000)**2
                h3_grid.loc[idx, f'{feature_name}_density_{radius}m'] = count / area_km2
        else:
            h3_grid.loc[idx, f'{feature_name}_nearest_distance'] = float('inf')
            for radius in radii:
                h3_grid.loc[idx, f'{feature_name}_count_{radius}m'] = 0
                h3_grid.loc[idx, f'{feature_name}_density_{radius}m'] = 0

    return h3_grid

def calculate_area_based_features(h3_grid, area_data, feature_name):
    """Рассчитывает признаки на основе пересечения с полигонами"""

    if area_data.empty:
        h3_grid[f'{feature_name}_coverage'] = 0
        h3_grid[f'{feature_name}_area_km2'] = 0
        return h3_grid

    # Фильтруем только полигоны
    polygons = area_data[area_data.geometry.geom_type.isin(['Polygon', 'MultiPolygon'])].copy()

    for idx, cell in h3_grid.iterrows():
        cell_geom = cell.geometry

        total_intersection_area = 0
        for _, area in polygons.iterrows():
            if area.geometry and not area.geometry.is_empty:
                try:
                    if cell_geom.intersects(area.geometry):
                        intersection = cell_geom.intersection(area.geometry)
                        # Приближенная площадь (в км²)
                        area_deg2 = intersection.area if hasattr(intersection, 'area') else 0
                        # Конвертируем из град² в км² (приблизительно)
                        area_km2 = area_deg2 * 111 * 111  # 1 градус ≈ 111 км
                        total_intersection_area += area_km2
                except:
                    continue

        # Площадь ячейки (в км²)
        cell_area_km2 = h3.average_hexagon_area(H3_RESOLUTION, 'km^2')

        # Доля покрытия ячейки
        coverage = min(total_intersection_area / cell_area_km2, 1.0) if cell_area_km2 > 0 else 0

        h3_grid.loc[idx, f'{feature_name}_coverage'] = coverage
        h3_grid.loc[idx, f'{feature_name}_area_km2'] = total_intersection_area

    return h3_grid

def calculate_road_network_features(h3_grid, roads_data):
    """Рассчитывает признаки дорожной сети"""

    if roads_data.empty:
        h3_grid['road_density'] = 0
        h3_grid['road_connectivity'] = 0
        return h3_grid

    for idx, cell in h3_grid.iterrows():
        cell_geom = cell.geometry

        total_road_length = 0
        intersections_count = 0

        for _, road in roads_data.iterrows():
            if road.geometry and not road.geometry.is_empty:
                try:
                    if cell_geom.intersects(road.geometry):
                        intersection = cell_geom.intersection(road.geometry)

                        # Длина дорог в ячейке (приблизительно в км)
                        if hasattr(intersection, 'length'):
                            length_deg = intersection.length
                            length_km = length_deg * 111  # 1 градус ≈ 111 км
                            total_road_length += length_km

                        intersections_count += 1
                except:
                    continue

        # Плотность дорог (км дорог на км² ячейки)
        cell_area_km2 = h3.average_hexagon_area(H3_RESOLUTION, 'km^2')
        road_density = total_road_length / cell_area_km2 if cell_area_km2 > 0 else 0

        h3_grid.loc[idx, 'road_density'] = road_density
        h3_grid.loc[idx, 'road_connectivity'] = intersections_count

    return h3_grid

